enum catapultState { REST, FIRING, MANUAL_OVERRIDE };
catapultState chooState = FIRING;

int twoCam = 1;

int speed = 0;

void setFeedSpeed(int speed)
{
	motor[wingus] = speed;
	motor[dingus] = speed;
}

void setChooSpeed(int speed)
{
	motor[choo1] = speed;
	motor[choo2] = speed;
	motor[cata1] = speed * twoCam;
	motor[cata1] = speed * twoCam;
}

void setRightSpeed(int speed)
{
	motor[right1] = speed;
	motor[right2] = speed;
}

void setLeftSpeed(int speed)
{
	motor[left1] = speed;
	motor[left2] = speed;
}

void updateTwoCam()
{
	if (vexRT[Btn8U] == 1 && time1[T1] > 500)
	{
		int twoCam = 1 - twoCam;
		clearTimer(T1);
	}
}

void feedControl()
{
	if (vexRT[Btn6U] == 1)
	{
		setFeedSpeed(127);
	}
	else if (vexRT[Btn6D] == 1)
	{
		setFeedSpeed(-127);
	}
	else
	{
		setFeedSpeed(0);
	}
}

void cataChooChoo()
{
	if (chooState == FIRING && SensorValue[chooSwitch] == 1)
	{
		//SensorValue[chooEncoder] = 0;
		chooState = REST;
	}
	else if (chooState == REST && vexRT[Btn5U] == 1)
	{
		chooState = FIRING;
	}
	else if (chooState == MANUAL_OVERRIDE && vexRT[Btn5D] == 0)
	{
		chooState = REST;
	}

	if (vexRT[Btn5D] == 1)
	{
		chooState = MANUAL_OVERRIDE;
	}

	switch (chooState)
	{
	case REST:
		speed = 20;
		break;
	case FIRING:
		speed = 127;
		break;
	case MANUAL_OVERRIDE:
		speed = 127;
		break;
	default:
		speed = 20;
	}

	setChooSpeed(speed);
}

task main()
{
	while (true)
	{
		updateTwoCam();

		cataChooChoo();

		feedControl();

		//drive control
		setRightSpeed(vexRT[Ch2]);
		setLeftSpeed(vexRT[Ch3]);
	}
}
